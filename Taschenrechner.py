num1 = float(input("Bitte die erste Zahl eingeben.: "))
operator = input("Bitte das Rechenzeichen eingeben.: ")
num2 = float(input("Bitte die zweite Zahl eingeben.: "))

if operator == "+":
    print(num1 + num2)
elif operator == "-":
    print(num1 - num2)
elif operator == "*":
    print(num1 * num2)
elif operator == "/":
    print(num1 / num2)
